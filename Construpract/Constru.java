package Construpract;

public class Constru {
    // Class Attributes
    int A = 20;


    Constru(){

        A = 10; // Set the initial value of Attributes

        A = 20;

        A = 25;

        A = 67;

    }

    public static void main(String[] args) {
        // Create a constructor
        Constru obj2 = new Constru();
        System.out.println(obj2.A);
    }


}
