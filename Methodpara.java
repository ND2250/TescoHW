public class Methodpara {

// Static method with pass the information to the method
// So it means parameter
// Call the parameter with variable name [ call means System.out.println ]

    static void geb (int data,String name){
        System.out.println(data + " " + name);


    }

    public static void amc ( float date,char alpha){
        System.out.println(date + " " + alpha );

    }

    public static void main(String[] args) {
        geb(3545,"Parivar");
        amc(04.0323f,'A');


    }
}
